            <li><strong>$arreglo = array(1, 2, 3);</strong> (Colección de valores)</li>
            <li><strong>$arreglo2 = ["Juan", "Pepa", "Luis"];</strong> (Arreglo con varios valores)</li>
            <li><strong>$nulo = null;</strong> (Representa la ausencia de valor)</li>
